package Shopping.Entity;

public class Banner {
	private int id;
	private String name_pro;
	private String img_pro;
	private String content;
	public Banner() {
		super();
	}

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName_pro() {
		return name_pro;
	}
	public void setName_pro(String name_pro) {
		this.name_pro = name_pro;
	}
	public String getImg_pro() {
		return img_pro;
	}
	public void setImg_pro(String img_pro) {
		this.img_pro = img_pro;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
}